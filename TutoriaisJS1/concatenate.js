// Existem varias maneiras de concatenar as variaveis...

let palavra01 = "Quero";

let palavra02 = "Um";

let palavra03 = "Chocolate";

// Exemplo 1 (forma um texto)
console.log(palavra01, palavra02, palavra03);

// Exemplo 2 (as variaveis ficam juntos)
console.log(palavra01 + palavra02 + palavra03);

// Exemplo 3 (em parceria com o amor que tenho pelo curso de Desenvolvimento de Sistemas XD)
console.log(`${palavra01} ${palavra02} ${palavra03} \n♥♥♥♥♥♥`);

// Exemplo 4 
console.log(palavra01, "um", palavra03);

// Exemplo 5
console.log(palavra01 + " um " + palavra03);

// Exemplo 6
console.log(`${palavra01} um ${palavra03}`);