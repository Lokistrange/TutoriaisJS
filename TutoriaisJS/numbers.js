let n1 = 200
let n2 = 401

console.log(n1)
console.log(n1, n2)
console.log(n1 + n2)

let soma = n1 + n2
console.log(`${n1} + ${n2} = ${soma}`)

let subtracao = n1 - n2
console.log(`${n1} - ${n2} = ${subtracao}`)

let multiplicacao = n1 * n2
console.log(`${n1} x ${n2} = ${multiplicacao}`)

let divisao = n1 / n2
console.log(`${n1} / ${n2} = ${divisao}`)

let potencia = Math.pow(n1, n2)
console.log(`${n1} ^ ${n2} = ${potencia}`)

console.log(Math.max(n1, n2))

// arredonda para um numero mais alto
console.log(Math.round(n1, n2))